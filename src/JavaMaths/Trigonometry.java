public class Trigonometry {   // Sin, cos and tan related calculations
    
    public static String unitOfMeasurement = "Radians";
    
    public static double sine(double num3){
            return (Math.sin(num3));
        }
    
    public static double cosine(double num3){
            return (Math.cos(num3));
        }
    
    public static double tangent(double num3){
            return (Math.tan(num3));
        }
}
