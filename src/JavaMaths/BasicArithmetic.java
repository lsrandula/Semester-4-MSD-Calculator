public class BasicArithmetic {     // Addition, Substraction, Multiplication and Division Related Calculations 
    
        public static String symbols = "Addition=+\nSubstraction=-\nMultiplication=*\nDivision=/";
    
        public static double addition(double num1, double num2){
            return (num1+num2);
        }
        
        public static double substraction(double num1, double num2){
            return (num1-num2);
        }
        
        public static double multiplication(double num1, double num2){
            return (num1*num2);
        }
        
        public static double division(double num1, double num2){
            return (num1/num2);
        }
    


}
