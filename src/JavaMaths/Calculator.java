    private void jButtonDecimalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDecimalActionPerformed
        String numberEntered = jScreen1.getText() + jButtonDecimal.getText();
        String temp = jScreen1.getText();
        boolean forLoopBroken = false;
        
        for (int i=0; i< temp.length(); i++){
            if (".".equals(Character.toString(temp.charAt(i)))){
                forLoopBroken = true;
                break;      // To limit the user from inputting more tha one "." which would leave to errors
            }
        }
        
        if (forLoopBroken == false){
            jScreen1.setText(numberEntered);
        }   
    }//GEN-LAST:event_jButtonDecimalActionPerformed

