    private void jButtonEqualSignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEqualSignActionPerformed
        
        try{
        if(this.operation == "+" | this.operation == "-" | this.operation == "*" | this.operation == "/"){    // Baisc Arithmetic
            this.number2 = Double.parseDouble(jScreen1.getText());

            if (this.operation == "+"){
                //System.out.println(this.number1+this.number2);
                jScreen1.setText(String.format("%.2f", BasicArithmetic.addition(this.number1, this.number2) ));
            }

            else if (this.operation == "-"){
                  jScreen1.setText(String.format("%.2f", BasicArithmetic.substraction(this.number1, this.number2) ));
            }

            else if (this.operation == "*"){
                  jScreen1.setText(String.format("%.2f", BasicArithmetic.multiplication(this.number1, this.number2) ));
            }

            else if (this.operation == "/"){
                  jScreen1.setText(String.format("%.2f", BasicArithmetic.division(this.number1, this.number2) ));
            }
            this.operation = "";
        }
        
        else{
            
            if (this.operation == "sin()"){
                
                this.number3 = Double.parseDouble(jScreen1.getText().substring(47)); // gets the value of x of sin(x)
                jScreen1.setText(String.format("%.2f", Trigonometry.sine(this.number3) ));
                //System.out.println(this.number3);
                this.operation = "";
            }
            
            else if (this.operation == "cos()"){
                
                this.number3 = Double.parseDouble(jScreen1.getText().substring(47)); // gets the value of x of cos(x)
                jScreen1.setText(String.format("%.2f", Trigonometry.cosine(this.number3) ));
                //System.out.println(this.number3);
                this.operation = "";
            }
            
            else if (this.operation == "tan()"){
                
                this.number3 = Double.parseDouble(jScreen1.getText().substring(47)); // gets the value of x of tan(x)
                jScreen1.setText(String.format("%.2f", Trigonometry.tangent(this.number3) ));
                //System.out.println(this.number3);
                this.operation = "";
            }
            
            else if (this.operation== "10toX"){
                this.number3 = Double.parseDouble(jScreen1.getText().substring(33)); // gets the value of x of 10^x
                jScreen1.setText(String.format("%.2f", Exponentials.tenToX(this.number3)));
                this.operation = "";
            }
            
            else if (this.operation== "XtoY_X"){
                this.number1 = Double.parseDouble(jScreen1.getText().substring(32)); // gets the value of x of x^y
                this.operation= "XtoY_Y";
                jScreen1.setText("x^y Calculation: Please Enter y=");
                
            }
            
            else if (this.operation== "XtoY_Y"){
                this.number2 = Double.parseDouble(jScreen1.getText().substring(32));    // gets the value of y of x^y
                jScreen1.setText(String.format("%.2f", Exponentials.xToY(this.number1, this.number2)));
                this.operation = "";
            }
            
            else if (this.operation== "EtoX"){
                this.number3 = Double.parseDouble(jScreen1.getText().substring(32)); // gets the value of x of e^x
                jScreen1.setText(String.format("%.2f", Exponentials.eToX(this.number3)));
                this.operation = "";
            }
            
            else if (this.operation== "logTen"){
                this.number3 = Double.parseDouble(jScreen1.getText().substring(37)); // gets the value of x of lon10(x)
                jScreen1.setText(String.format("%.2f", Logarithms.logTen(this.number3)));
                this.operation = "";
            }
            
            else if (this.operation== "logN"){
                this.number3 = Double.parseDouble(jScreen1.getText().substring(34)); // gets the value of x of logN(x)
                jScreen1.setText(String.format("%.2f", Logarithms.logN(this.number3)));
                this.operation = "";
            }
            
            else if (this.operation== "sqRoot"){
                this.number3 = Double.parseDouble(jScreen1.getText().substring(45)); // gets the value of x of sqrt(x)
                jScreen1.setText(String.format("%.2f", Exponentials.sqRoot(this.number3)));
                this.operation = "";
            }
            
            else if (this.operation== "factOfX"){
                this.number3 = Double.parseDouble(jScreen1.getText().substring(41)); // gets the value of x of factorial(x)
                this.number4 = (int)this.number3;
                //System.out.println(this.number4);
                if (this.number4<=12){
                    jScreen1.setText(String.format("%d", Factorials.factorialOfX(this.number4)));
                }
                else{
                    JOptionPane.showMessageDialog(null, "<html><font size = '15'>Overflow occured!!", " '10'>Error", JOptionPane.INFORMATION_MESSAGE);
                    jScreen1.setText("");
                }
                
                this.operation = "";
            }
            
            else{
                JOptionPane.showMessageDialog(null, "<html><font size = '15'>Please enter an input!!", " '10'>Error", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        jButtonAdd.setEnabled(true);
        jButtonSubstract.setEnabled(true);
        jButtonMultiply.setEnabled(true);
        jButtonDivide.setEnabled(true);
        jButtonNegative.setEnabled(true);
        }
        
        catch(NumberFormatException ex){
            //System.out.println("NumberFormatException");   // catches the error which occurs when no input has been entered
            //jScreen1.setText("Please enter a number for x");
            //JOptionPane.showMessageDialog(frame, "Error");
            JOptionPane.showMessageDialog(null, "<html><font size = '15'>Please enter a value for x!!", " '10'>Error", JOptionPane.INFORMATION_MESSAGE);
        }
        
        catch(StackOverflowError ex){
               JOptionPane.showMessageDialog(null, "<html><font size = '15'>Value too large to calculate!!", " '10'>Error", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButtonEqualSignActionPerformed

